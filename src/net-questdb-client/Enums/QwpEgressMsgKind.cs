namespace QuestDB.Enums;

/// <summary>QWP egress message kind discriminator (first byte of every egress payload).</summary>
public enum QwpEgressMsgKind : byte
{
    QueryRequest = 0x10,
    ResultBatch = 0x11,
    ResultEnd = 0x12,
    QueryError = 0x13,
    Cancel = 0x14,
    Credit = 0x15,
    ExecDone = 0x16,
    CacheReset = 0x17,
    ServerInfo = 0x18,
}
